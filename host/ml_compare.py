def export_edge_impulse(X, y, feature_names, filename='edge_impulse_data.csv'):
    """Export data in Edge Impulse format."""
    with open(filename, 'w') as f:
        # Edge Impulse expects: timestamp, features..., label
        header = ['timestamp'] + feature_names + ['label']
        f.write(','.join(header) + '\n')

        for i, (features, label) in enumerate(zip(X, y)):
            timestamp = i * 20  # Assume 50Hz = 20ms intervals
            row = [str(timestamp)] + [f'{v:.6f}' for v in features] + [CLASS_NAMES[label]]
            f.write(','.join(row) + '\n')

    print(f"  Exported for Edge Impulse: {filename}")
    print("  Upload to Edge Impulse Studio for cloud training")


