def train_tensorflow(X_train, X_test, y_train, y_test, output_dir='.'):
    """Train TensorFlow model and convert to TFLite."""
    try:
        import tensorflow as tf
        import numpy as np
    except ImportError:
        print("  TensorFlow not installed, skipping...")
        return None

    # Convert to numpy
    X_train_np = np.array(X_train, dtype=np.float32)
    X_test_np = np.array(X_test, dtype=np.float32)
    y_train_np = np.array(y_train, dtype=np.int32)
    y_test_np = np.array(y_test, dtype=np.int32)

    # Build model
    model = tf.keras.Sequential([
        tf.keras.layers.Dense(16, activation='relu', input_shape=(X_train_np.shape[1],)),
        tf.keras.layers.Dense(8, activation='relu'),
        tf.keras.layers.Dense(NUM_CLASSES, activation='softmax')
    ])

    model.compile(optimizer='adam',
                  loss='sparse_categorical_crossentropy',
                  metrics=['accuracy'])

    print("  Training TensorFlow model...")
    model.fit(X_train_np, y_train_np, epochs=50, batch_size=32,
              validation_split=0.1, verbose=0)

    # Evaluate
    loss, acc = model.evaluate(X_test_np, y_test_np, verbose=0)
    print(f"  TensorFlow: {acc*100:.1f}%")

    # Save full model
    model.save(f'{output_dir}/motion_model.keras')
    print(f"  Saved: {output_dir}/motion_model.keras")

    # Convert to TFLite
    converter = tf.lite.TFLiteConverter.from_keras_model(model)
    converter.optimizations = [tf.lite.Optimize.DEFAULT]
    converter.target_spec.supported_types = [tf.float16]
    tflite_model = converter.convert()

    tflite_path = f'{output_dir}/motion_model.tflite'
    with open(tflite_path, 'wb') as f:
        f.write(tflite_model)
    print(f"  Saved: {tflite_path} ({len(tflite_model)} bytes)")

    # Generate C header for embedded deployment
    generate_tflite_header(tflite_model, f'{output_dir}/motion_model.h')

    return acc


def generate_tflite_header(tflite_model, filename):
    """Generate C header file for TFLite model."""
    with open(filename, 'w') as f:
        f.write('// TFLite model for motion detection\n')
        f.write('// Generated by ml_compare.py\n\n')
        f.write(f'const unsigned int motion_model_len = {len(tflite_model)};\n')
        f.write('const unsigned char motion_model[] = {\n  ')

        for i, byte in enumerate(tflite_model):
            f.write(f'0x{byte:02x}')
            if i < len(tflite_model) - 1:
                f.write(', ')
                if (i + 1) % 12 == 0:
                    f.write('\n  ')

        f.write('\n};\n')
    print(f"  Saved: {filename}")


