def train_sklearn(X_train, X_test, y_train, y_test):
    """Train scikit-learn models for comparison."""
    try:
        from sklearn.neighbors import KNeighborsClassifier
        from sklearn.tree import DecisionTreeClassifier
        from sklearn.ensemble import RandomForestClassifier
        from sklearn.neural_network import MLPClassifier
        from sklearn.svm import SVC
        from sklearn.metrics import accuracy_score
    except ImportError:
        print("  scikit-learn not installed, skipping...")
        return {}

    results = {}
    models = {
        'sklearn_KNN': KNeighborsClassifier(n_neighbors=5),
        'sklearn_Tree': DecisionTreeClassifier(max_depth=5),
        'sklearn_Forest': RandomForestClassifier(n_estimators=10, max_depth=5),
        'sklearn_MLP': MLPClassifier(hidden_layer_sizes=(10,), max_iter=500, random_state=42),
        'sklearn_SVM': SVC(kernel='rbf', random_state=42),
    }

    for name, model in models.items():
        model.fit(X_train, y_train)
        pred = model.predict(X_test)
        acc = accuracy_score(y_test, pred)
        results[name] = acc
        print(f"  {name}: {acc*100:.1f}%")

    return results


