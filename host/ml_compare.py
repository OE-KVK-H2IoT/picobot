class PythonDecisionTree:
    """Decision Tree - pure Python, runs on Pico."""

    def __init__(self, max_depth=5):
        self.max_depth = max_depth
        self.tree = None

    def _gini(self, y):
        if not y:
            return 0
        counts = Counter(y)
        n = len(y)
        return 1 - sum((c/n)**2 for c in counts.values())

    def _best_split(self, X, y):
        best_gain, best_feat, best_thresh = -1, None, None
        current_gini = self._gini(y)

        for feat in range(len(X[0])):
            values = sorted(set(x[feat] for x in X))
            for i in range(len(values) - 1):
                thresh = (values[i] + values[i+1]) / 2
                left_y = [y[j] for j, x in enumerate(X) if x[feat] <= thresh]
                right_y = [y[j] for j, x in enumerate(X) if x[feat] > thresh]

                if not left_y or not right_y:
                    continue

                gain = current_gini - (len(left_y)/len(y) * self._gini(left_y) +
                                       len(right_y)/len(y) * self._gini(right_y))
                if gain > best_gain:
                    best_gain, best_feat, best_thresh = gain, feat, thresh

        return best_feat, best_thresh

    def _build(self, X, y, depth):
        if depth >= self.max_depth or len(set(y)) == 1 or len(X) < 5:
            return {'leaf': True, 'class': Counter(y).most_common(1)[0][0]}

        feat, thresh = self._best_split(X, y)
        if feat is None:
            return {'leaf': True, 'class': Counter(y).most_common(1)[0][0]}

        left_X = [X[i] for i in range(len(X)) if X[i][feat] <= thresh]
        left_y = [y[i] for i in range(len(X)) if X[i][feat] <= thresh]
        right_X = [X[i] for i in range(len(X)) if X[i][feat] > thresh]
        right_y = [y[i] for i in range(len(X)) if X[i][feat] > thresh]

        return {
            'leaf': False, 'feature': feat, 'threshold': thresh,
            'left': self._build(left_X, left_y, depth + 1),
            'right': self._build(right_X, right_y, depth + 1)
        }

    def fit(self, X, y):
        self.tree = self._build(X, y, 0)

    def _predict_one(self, x, node):
        if node['leaf']:
            return node['class']
        if x[node['feature']] <= node['threshold']:
            return self._predict_one(x, node['left'])
        return self._predict_one(x, node['right'])

    def predict(self, X):
        return [self._predict_one(x, self.tree) for x in X]

    def export_micropython(self, means, stds, filename='tree_model.py'):
        """Export as if-else chain for MicroPython."""

        def tree_to_code(node, indent="    "):
            if node['leaf']:
                return f"{indent}return {node['class']}"
            code = f"{indent}if x[{node['feature']}] <= {node['threshold']:.6f}:\n"
            code += tree_to_code(node['left'], indent + "    ") + "\n"
            code += f"{indent}else:\n"
            code += tree_to_code(node['right'], indent + "    ")
            return code

        code = f'''"""Decision Tree Model - Generated by ml_compare.py"""

MEANS = {means}
STDS = {stds}

def normalize(x):
    return [(x[i] - MEANS[i]) / STDS[i] for i in range(len(x))]

def predict(x):
    x = normalize(x)
{tree_to_code(self.tree)}
'''
        with open(filename, 'w') as f:
            f.write(code)
        print(f"  Exported: {filename}")


