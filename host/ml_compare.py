def load_data(filename):
    """Load training data from CSV."""
    X, y = [], []
    feature_names = None

    with open(filename, 'r') as f:
        reader = csv.reader(f)
        header = next(reader)
        feature_names = header[:-2]  # Exclude 'label' and 'label_name'

        for row in reader:
            if len(row) < 3:
                continue
            features = [float(x) for x in row[:-2]]
            label = int(row[-2])
            X.append(features)
            y.append(label)

    print(f"Loaded {len(X)} samples, {len(feature_names)} features")
    print(f"Features: {', '.join(feature_names)}")

    # Class distribution
    counts = Counter(y)
    print("\nClass distribution:")
    for label in sorted(counts.keys()):
        pct = 100 * counts[label] / len(y)
        bar = '#' * int(pct / 2)
        print(f"  {CLASS_NAMES[label]:6s}: {counts[label]:4d} ({pct:5.1f}%) {bar}")

    return X, y, feature_names


def split_data(X, y, test_ratio=0.2, seed=42):
    """Split into train/test sets."""
    random.seed(seed)
    indices = list(range(len(X)))
    random.shuffle(indices)

    split = int(len(indices) * (1 - test_ratio))
    train_idx, test_idx = indices[:split], indices[split:]

    X_train = [X[i] for i in train_idx]
    y_train = [y[i] for i in train_idx]
    X_test = [X[i] for i in test_idx]
    y_test = [y[i] for i in test_idx]

    return X_train, X_test, y_train, y_test


def normalize_data(X_train, X_test):
    """Normalize features (z-score)."""
    n_features = len(X_train[0])

    means = [sum(x[i] for x in X_train) / len(X_train) for i in range(n_features)]
    stds = [math.sqrt(sum((x[i] - means[i])**2 for x in X_train) / len(X_train)) or 1.0
            for i in range(n_features)]

    X_train_norm = [[(x[i] - means[i]) / stds[i] for i in range(n_features)] for x in X_train]
    X_test_norm = [[(x[i] - means[i]) / stds[i] for i in range(n_features)] for x in X_test]

    return X_train_norm, X_test_norm, means, stds


